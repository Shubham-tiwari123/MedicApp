package com.medical.client.entity;

public class GetKeys {
    private String modulesHashValue;
    private String modulesValue;
    private String expoValue;
    private String expoHashValue;

    public String getModulesHashValue() {
        return modulesHashValue;
    }

    public void setModulesHashValue(String modulesHashValue) {
        this.modulesHashValue = modulesHashValue;
    }

    public String getModulesValue() {
        return modulesValue;
    }

    public void setModulesValue(String modulesValue) {
        this.modulesValue = modulesValue;
    }

    public String getExpoValue() {
        return expoValue;
    }

    public void setExpoValue(String expoValue) {
        this.expoValue = expoValue;
    }

    public String getExpoHashValue() {
        return expoHashValue;
    }

    public void setExpoHashValue(String expoHashValue) {
        this.expoHashValue = expoHashValue;
    }
}
